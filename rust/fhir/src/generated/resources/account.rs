/// A financial tool for tracking value accrued for a particular purpose.  In the
/// healthcare field, used to track charges for a patient, cost centers, etc.
#[derive(Debug, Clone, PartialEq)]
pub struct Account {
    pub subject: array,
    pub balance: array,
    pub meta: Meta,
    pub diagnosis: array,
    pub text: Narrative,
    pub currency: CodeableConcept,
    pub service_period: Period,
    pub identifier: array,
    pub implicit_rules: uri,
    pub coverage: array,
    pub status: code,
    pub extension: array,
    pub owner: Reference,
    pub related_account: array,
    pub calculated_at: instant,
    pub contained: array,
    pub billing_status: CodeableConcept,
    pub id: id,
    pub modifier_extension: array,
    pub guarantor: array,
    pub procedure: array,
    pub language: code,
    pub name: string,
    pub resource_type: Account,
    pub r#type: CodeableConcept,
    pub description: markdown,
}
